MainWindow::MainWindow(QWidget *parent) :
    QMainWindow(parent),
    ui(new Ui::MainWindow)
{
    ui->setupUi(this);
}

MainWindow::~MainWindow()
{
    delete ui;
}

void MainWindow::on_GLSLTextEdit_textChanged()
{
    ui->glWidget->calculate(ui->GLSLTextEdit->toPlainText());
}
